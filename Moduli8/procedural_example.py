#funksionon pa class direkt me funksionin def
def calculate_area(length,width):
    return length*width


def calculate_perimeter(length,width):
    return 2*(length+width)


length = 5
width = 3

area_calculated = calculate_area(length,width)

perimeter_calculated = calculate_perimeter(length,width)


print(f"The area of the rectangle is {area_calculated}")

print(f"The perimeter of the rectangle is {perimeter_calculated}")

